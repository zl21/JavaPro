class Demo1_Constant {
	public static void main(String[] args) {
		System.out.println("abc");		//字符串常量
		System.out.println(123);		//整数
		System.out.println(12.3);		//小数
		System.out.println('1');		//字符常量，用单引号括起来的内容，只能是单个数字/字母/符号，不能为空！
		System.out.println(' ');		//字符常量，空格！
		System.out.println(true);		//布尔常量，Boolean：true/false
	}
}