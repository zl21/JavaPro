/*
(private关键字的概述和特点)
* A:人类赋值年龄的问题
* B:private关键字特点
	* a:是一个权限修饰符
	* b:可以修饰成员变量和成员方法
	* c:被其修饰的成员只能在本类中被访问
* 封装和private的应用：
	* A:把成员变量用private修饰
	* B:提供对应的getXxx()和setXxx()方法
	* private仅仅是封装的一种体现形式,不能说封装就是私有
*/
	
class Demo4_Person01 {
	public static void main(String[] args) {
		Person p1 = new Person();
		p1.name = "张三";
		//p.age = 23;
		//p.speak();
		p1.setAge(-23);

		System.out.println(p1.name + p1.getAge());

		System.out.println("-------------分割线-------------");
	}
}

class Person {
	String name;
	private int age;

	public void setAge(int a) {		//设置年龄
		if (a > 0 && a < 200) {
			age = a;
		}else {
			System.out.println("年龄错误XXX！");
		}
	}

	public int getAge() {			//获取年龄
		return age;
	}

	public void speak() {
		System.out.println(name + "今年" + age);
	}
}
